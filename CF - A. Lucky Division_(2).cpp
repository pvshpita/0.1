#include <iostream>
using namespace std;

bool lucky (int num)
{
    while (num > 0) {
        int dig = num % 10;
        if (dig != 4 && dig != 7) {
            return false;
        }
        num /= 10;
    }
    return true;
}

int main ()
{
    int n;
    cin >> n;

    for (int i = 1; i <= 1000; ++i) {
        if (lucky(i) && n % i == 0) {
            cout << "YES\n";
            return 0;
        }
    }

    cout << "NO\n";

    return 0;
}

